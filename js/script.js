function showNav () {
    var div = document.getElementById('nav-mobile');
    div.style.display = 'block';
}

function closeNav () {
    var div = document.getElementById('nav-mobile');
    div.style.display = 'none';
}

function showOverlay () {
    var div = document.getElementById('overlay');
    div.style.display = 'block';
}

function closeOverlay () {
    var div = document.getElementById('overlay');
    div.style.display = 'none';
}